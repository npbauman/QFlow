def F_2(Fock_Trans, V_Trans, t1, t2, f, g, o, v):

    #   HH  ------------------------------------------------------------
    #   -(1.0/2.0) * t1(e, i) * f(m, j) * t1(e, m)
    Fock_Trans[o,o] += -0.5 * einsum('ei,mj,em->ij', t1, f[o, o], t1, optimize=True)
    #   -(1.0/2.0) * t1(e, m) * f(i, m) * t1(e, j)
    Fock_Trans[o,o] += -0.5 * einsum('em,im,ej->ij', t1, f[o, o], t1, optimize=True)
    #   1.0 * t1(e, i) * f(e, f) * t1(f, j)
    Fock_Trans[o,o] += 1.0 * einsum('ei,ef,fj->ij', t1, f[v, v], t1, optimize=True)
    #   (1.0/2.0) * t1(e, i) * f(m, f) * t2(e, f, j, m)
    Fock_Trans[o,o] += 0.5 * einsum('ei,mf,efjm->ij', t1, f[o, v], t2, optimize=True)
    #   -(1.0/2.0) * t1(e, m) * f(i, f) * t2(e, f, j, m)
    Fock_Trans[o,o] += -0.5 * einsum('em,if,efjm->ij', t1, f[o, v], t2, optimize=True)
    #   -(1.0/4.0) * t2(e, f, m, i) * f(n, j) * t2(e, f, m, n)
    Fock_Trans[o,o] += -0.25 * einsum('efmi,nj,efmn->ij', t2, f[o, o], t2, optimize=True)
    #   -(1.0/4.0) * t2(e, f, m, n) * f(i, m) * t2(e, f, j, n)
    Fock_Trans[o,o] += -0.25 * einsum('efmn,im,efjn->ij', t2, f[o, o], t2, optimize=True)
    #   (1.0/2.0) * t2(e, f, m, i) * f(n, m) * t2(e, f, j, n)
    Fock_Trans[o,o] += 0.5 * einsum('efmi,nm,efjn->ij', t2, f[o, o], t2, optimize=True)
    #   1.0 * t2(e, f, m, i) * f(e, g) * t2(f, g, j, m)
    Fock_Trans[o,o] += 1.0 * einsum('efmi,eg,fgjm->ij', t2, f[v, v], t2, optimize=True)
    #   -(1.0/2.0) * t2(e, f, m, i) * f(j, e) * t1(f, m)
    Fock_Trans[o,o] += -0.5 * einsum('efmi,je,fm->ij', t2, f[o, v], t1, optimize=True)
    #   (1.0/2.0) * t2(e, f, m, i) * f(m, e) * t1(f, j)
    Fock_Trans[o,o] += 0.5 * einsum('efmi,me,fj->ij', t2, f[o, v], t1, optimize=True)

    #   PP  ------------------------------------------------------------
    #   -(1.0/2.0) * t1(b, m) * f(a, e) * t1(e, m)
    Fock_Trans[v,v] += -0.5 * einsum('bm,ae,em->ab', t1, f[v, v], t1, optimize=True)
    #   1.0 * t1(b, m) * f(n, m) * t1(a, n)
    Fock_Trans[v,v] += 1.0 * einsum('bm,nm,an->ab', t1, f[o, o], t1, optimize=True)
    #   -(1.0/2.0) * t1(e, m) * f(e, b) * t1(a, m)
    Fock_Trans[v,v] += -0.5 * einsum('em,eb,am->ab', t1, f[v, v], t1, optimize=True)
    #   -(1.0/2.0) * t1(b, m) * f(n, e) * t2(a, e, m, n)
    Fock_Trans[v,v] += -0.5 * einsum('bm,ne,aemn->ab', t1, f[o, v], t2, optimize=True)
    #   (1.0/2.0) * t1(e, m) * f(n, b) * t2(a, e, m, n)
