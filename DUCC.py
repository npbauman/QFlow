def H_0(Fock_Trans, V_Trans, t1, t2, f, g, o, v):

    Fock_Trans[o,o] += 1.0 * einsum('ij->ij', f[o, o])
    Fock_Trans[o,v] += 1.0 * einsum('ia->ia', f[o, v])
    Fock_Trans[v,v] += 1.0 * einsum('ab->ab', f[v, v])
    V_Trans[o,o,o,o] += 1.0 * einsum('ijkl->ijkl', g[o, o, o, o])
    V_Trans[o,o,o,v] += 1.0 * einsum('ijka->ijka', g[o, o, o, v])
    V_Trans[o,o,v,v] += 1.0 * einsum('ijab->ijab', g[o, o, v, v])
    V_Trans[o,v,o,v] += 1.0 * einsum('iajb->iajb', g[o, v, o, v])
    V_Trans[o,v,v,v] += 1.0 * einsum('iabc->iabc', g[o, v, v, v])
    V_Trans[v,v,v,v] += 1.0 * einsum('abcd->abcd', g[v, v, v, v])

def F_1(Fock_Trans, V_Trans, t1, t2, f, g, o, v):

    #   HH  ------------------------------------------------------------
    #   1.0 * f(i, e) * t1(e, j)
    Fock_Trans[o,o] += 1.0 * einsum('ie,ej->ij', f[o, v], t1, optimize=True)
    #   1.0 * t1(e, i) * f(j, e)
    Fock_Trans[o,o] += 1.0 * einsum('ei,je->ij', t1, f[o, v], optimize=True)

    #   PP  ------------------------------------------------------------
    #   -1.0 * f(m, b) * t1(a, m)
    Fock_Trans[v,v] += -1.0 * einsum('mb,am->ab', f[o, v], t1, optimize=True)
    #   -1.0 * t1(b, m) * f(m, a)
    Fock_Trans[v,v] += -1.0 * einsum('bm,ma->ab', t1, f[o, v], optimize=True)

    #   HP  ------------------------------------------------------------
    #   -1.0 * f1_OO(m, i) * t1(a, m)
    Fock_Trans[o,v] += -1.0 * einsum('mi,am->ia', f[o, o], t1, optimize=True)
    #   1.0 * f1_VV(a, e) * t1(e, i)
    Fock_Trans[o,v] += 1.0 * einsum('ae,ei->ia', f[v, v], t1, optimize=True)
    #   1.0 * f1_OV(m, e) * t2(a, e, i, m)
    Fock_Trans[o,v] += 1.0 * einsum('me,aeim->ia', f[o, v], t2, optimize=True)

    #   HHHH -----------------------------------------------------------
    #   NO TERMS

    #   HHHP -----------------------------------------------------------
    #   1.0 * f1_OV(k, e) * t2(a, e, j, i)
    V_Trans[o,o,o,v] += 1.0 * einsum('ke,aeji->ijka', f[o, v], t2, optimize=True)

    #   HHPP -----------------------------------------------------------
    #   1.0 * f1_OO(m, i) * t2(a, b, j, m)
    V_Trans[o,o,v,v] += 1.0 * einsum('mi,abjm->ijab', f[o, o], t2, optimize=True)
    #   -1.0 * f1_VV(a, e) * t2(b, e, i, j)
    V_Trans[o,o,v,v] += -1.0 * einsum('ae,beij->ijab', f[v, v], t2, optimize=True)
    #   -1.0 * f1_OO(m, j) * t2(a, b, i, m)
    V_Trans[o,o,v,v] += -1.0 * einsum('mj,abim->ijab', f[o, o], t2, optimize=True)
    #   1.0 * f1_VV(b, e) * t2(a, e, i, j)
    V_Trans[o,o,v,v] += 1.0 * einsum('be,aeij->ijab', f[v, v], t2, optimize=True)

    #   HPHP -----------------------------------------------------------
    #   NO TERMS

    #   HPPP -----------------------------------------------------------
    #   -1.0 * f1_OV(m, a) * t2(b, c, i, m)
