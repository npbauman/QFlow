    #   PP  ------------------------------------------------------------
    #   -1.0 * v2iabc(m, a, b, e) * t1(e, m)
    Fock_Trans[v,v] += -1.0 * einsum('mabe,em->ab', g[o, v, v, v], t1, optimize=False)
    #   (1.0/2.0) * v2ijab(m, n, e, b) * t2(a, e, m, n)
    Fock_Trans[v,v] += 0.5 * einsum('mneb,aemn->ab', g[o, o, v, v], t2, optimize=True)
    #   -1.0 * t1(e, m) * v2iabc(m, b, a, e)
    Fock_Trans[v,v] += -1.0 * einsum('em,mbae->ab', t1, g[o, v, v, v], optimize=True)
    #   (1.0/2.0) * t2(e, b, m, n) * v2ijab(m, n, a, e)
    Fock_Trans[v,v] += 0.5 * einsum('ebmn,mnae->ab', t2, g[o, o, v, v], optimize=True)

    #   PH  ------------------------------------------------------------
    #   -1.0 * v2iajb(i, e, m, a) * t1(e, m)
    Fock_Trans[o,v] += -1.0 * einsum('iema,em->ia', g[o, v, o, v], t1, optimize=True)
    #   -(1.0/2.0) * v2ijka(m, n, i, e) * t2(a, e, m, n)
    Fock_Trans[o,v] += -0.5 * einsum('mnie,aemn->ia', g[o, o, o, v], t2, optimize=True)
    #   (1.0/2.0) * v2iabc(m, a, f, e) * t2(e, f, i, m)
    Fock_Trans[o,v] += 0.5 * einsum('mafe,efim->ia', g[o, v, v, v], t2, optimize=True)
    #   1.0 * t1(e, m) * v2ijab(i, m, a, e)
    Fock_Trans[o,v] += 1.0 * einsum('em,imae->ia', t1, g[o, o, v ,v], optimize=True)

    #   HHHH -----------------------------------------------------------
    #   1.0 * v2ijka(i, j, k, e) * t1(e, l)
    V_Trans[o,o,o,o] += 1.0 * einsum('ijke,el->ijkl', g[o, o, o, v], t1, optimize=True)
    #   -1.0 * v2ijka(i, j, l, e) * t1(e, k)
    V_Trans[o,o,o,o] += -1.0 * einsum('ijle,ek->ijkl', g[o, o, o, v], t1, optimize=True)
    #   (1.0/2.0) * v2ijab(i, j, e, f) * t2(e, f, k, l)
    V_Trans[o,o,o,o] += (1.0/2.0) * einsum('ijef,efkl->ijkl', g[o, o, v, v], t2, optimize=True)
    #   1.0 * t1(e, i) * v2ijka(l, k, j, e)
    V_Trans[o,o,o,o] += 1.0 * einsum('ei,lkje->ijkl', t1, g[o, o, o, v], optimize=True)
    #   -1.0 * t1(e, j) * v2ijka(l, k, i, e)
    V_Trans[o,o,o,o] += -1.0 * einsum('ej,lkie->ijkl', t1, g[o, o, o, v], optimize=True)
    #   (1.0/2.0) * t2(e, f, i, j) * v2ijab(k, l, e, f)
    V_Trans[o,o,o,o] += (1.0/2.0) * einsum('efij,klef->ijkl', t2, g[o, o, v, v], optimize=True)

    #   HHHP -----------------------------------------------------------
    #   -1.0 * v2iajb(k, a, j, e) * t1(e, i)
