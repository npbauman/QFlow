    #   -1.0 * v2iajb(i, e, m, a) * t2(b, e, j, m)
    V_Trans[o,o,v,v] += -1.0 * einsum('iema,bejm->ijab', g[o, v, o, v], t2, optimize=True)
    #   (1.0/2.0) * v2ijkl(m, n, i, j) * t2(a, b, m, n)
