    V_Trans[o,o,o,v] += 1.0 * einsum('kaie,ej->ijka', g[o, v, o, v], t1, optimize=True)
    #   1.0 * v2ijka(m, k, j, e) * t2(a, e, i, m)
    V_Trans[o,o,o,v] += 1.0 * einsum('mkje,aeim->ijka', g[o, o, o, v], t2, optimize=True)
    #   (1.0/2.0) * v2iabc(k, a, f, e) * t2(e, f, j, i)
    V_Trans[o,o,o,v] += (1.0/2.0) * einsum('kafe,efji->ijka', g[o, v, v, v], t2, optimize=True)
    #   -1.0 * v2ijka(m, k, i, e) * t2(a, e, j, m)
    V_Trans[o,o,o,v] += -1.0 * einsum('mkie,aejm->ijka', g[o, o, o, v], t2, optimize=True)
    #   1.0 * t1(e, k) * v2ijab(j, i, a, e)
    V_Trans[o,o,o,v] += 1.0 * einsum('ek,jiae->ijka', t1, g[o, o, v, v], optimize=True)

    #   HHPP -----------------------------------------------------------
    #   -1.0 * v2ijka(j, i, m, a) * t1(b, m)
    V_Trans[o,o,v,v] += -1.0 * einsum('jima,bm->ijab', g[o, o, o, v], t1, optimize=True)
    #   1.0 * v2iabc(i, e, a, b) * t1(e, j)
    V_Trans[o,o,v,v] += 1.0 * einsum('ieab,ej->ijab', g[o, v, v, v], t1, optimize=True)
    #   1.0 * v2ijka(j, i, m, b) * t1(a, m)
